using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Cvm.Web.Facade.Util
{
    class AppUtil
    {
        public static string ToStr(DateTime? d)
        {
            return d != null ? d.Value.ToShortDateString() : "";
        }
        public static string ToStr(long? d)
        {
            return d != null ? "" +d.Value : "";
        }
    }
}
